package Models;

public class Customer {
	private int Customer_ID;
    private String Customer_Full_Name;
    private String Customer_Email;
    private String Customer_NIC;
    private String Customer_Address;
    private String Customer_Telephone_Number;
    private String Customer_Password;
    
	public int getCustomer_ID() {
		return Customer_ID;
	}
	public void setCustomer_ID(int customer_ID) {
		Customer_ID = customer_ID;
	}
	public String getCustomer_Full_Name() {
		return Customer_Full_Name;
	}
	public void setCustomer_Full_Name(String customer_Full_Name) {
		Customer_Full_Name = customer_Full_Name;
	}
	public String getCustomer_Email() {
		return Customer_Email;
	}
	public void setCustomer_Email(String customer_Email) {
		Customer_Email = customer_Email;
	}
	public String getCustomer_NIC() {
		return Customer_NIC;
	}
	public void setCustomer_NIC(String customer_NIC) {
		Customer_NIC = customer_NIC;
	}
	public String getCustomer_Address() {
		return Customer_Address;
	}
	public void setCustomer_Address(String customer_Address) {
		Customer_Address = customer_Address;
	}
	public String getCustomer_Telephone_Number() {
		return Customer_Telephone_Number;
	}
	public void setCustomer_Telephone_Number(String customer_Telephone_Number) {
		Customer_Telephone_Number = customer_Telephone_Number;
	}
	public String getCustomer_Password() {
		return Customer_Password;
	}
	public void setCustomer_Password(String customer_Password) {
		Customer_Password = customer_Password;
	}
}